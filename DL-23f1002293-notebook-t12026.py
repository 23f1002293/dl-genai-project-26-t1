    "class MusicDataset(Dataset):\n",
    "    def __init__(self, samples, augment=True):\n",
    "        self.samples = samples\n",
    "        self.augment = augment\n",
    "        self.target_samples = CFG.SR * CFG.DURATION\n",
    "        \n",
    "        self.genre_pool = {i: [] for i in range(len(CFG.GENRES))}\n",
    "        for label, stems in samples:\n",
    "            self.genre_pool[label].append(stems)\n",
    "            \n",
    "        self.noise_files = glob.glob(os.path.join(CFG.ESC50_ROOT, 'audio', '*.wav')) if augment else []\n",
    "\n",
    "    def load_audio(self, path):\n",
    "        wav, sr = sf.read(path)\n",
    "        wav = torch.from_numpy(wav).float()\n",
    "        \n",
    "        if len(wav.shape) > 1:\n",
    "            wav = torch.mean(wav, dim=1, keepdim=True).T\n",
    "        else:\n",
    "            wav = wav.unsqueeze(0)\n",
    "            \n",
    "        if sr != CFG.SR:\n",
    "            resampler = T.Resample(sr, CFG.SR)\n",
    "            wav = resampler(wav)\n",
    "            \n",
    "        return wav.squeeze(0)\n",
    "\n",
    "    def __len__(self):\n",
    "        return len(self.samples)\n",
    "\n",
    "    def __getitem__(self, idx):\n",
    "        label, _ = self.samples[idx]\n",
    "        mix = torch.zeros(self.target_samples)\n",
    "\n",
    "        for stem in CFG.STEM_KEYS:\n",
    "            # Stage 2: Pick a random song from the same genre for THIS stem\n",
    "            random_stems = random.choice(self.genre_pool[label])\n",
    "            y = self.load_audio(random_stems[stem])\n",
    "            \n",
    "            if y.shape[0] > self.target_samples:\n",
    "                if not self.augment: \n",
    "                    start = (y.shape[0] - self.target_samples) // 2\n",
    "                else:\n",
    "                    start = random.randint(0, y.shape[0] - self.target_samples)\n",
    "                y = y[start : start + self.target_samples]\n",
    "            else:\n",
    "                y = torch.nn.functional.pad(y, (0, self.target_samples - y.shape[0]))\n",
    "\n",
    "            rms = torch.sqrt(torch.mean(y**2) + 1e-9)\n",
    "            y = y / rms\n",
    "            \n",
    "            vol = random.uniform(0.4, 1.1) if self.augment else 0.8\n",
    "            mix += y * vol\n",
    "\n",
    "        mix = mix / (torch.max(torch.abs(mix)) + 1e-6)\n",
    "\n",
