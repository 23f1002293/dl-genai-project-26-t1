    "                \n",
    "                audio_len = len(full_audio)\n",
    "                if audio_len > target_samples:\n",
    "                    # 3 windows: Start, Middle, End\n",
    "                    offsets = [0, (audio_len - target_samples)//2, audio_len - target_samples]\n",
    "                else:\n",
    "                    offsets = [0]\n",
    "                \n",
    "                all_logits = []\n",
