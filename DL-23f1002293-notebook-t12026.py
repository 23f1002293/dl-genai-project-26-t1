    "def get_files(data_dir):\n",
    "    genre_data = {}\n",
    "    search_path = os.path.join(data_dir, \"genres_stems\")\n",
    "    if not os.path.exists(search_path): search_path = data_dir\n",
    "    \n",
    "    genres = sorted([d for d in os.listdir(search_path) \n",
    "                   if os.path.isdir(os.path.join(search_path, d)) \n",
    "                   and not d.startswith('.') \n",
    "                   and d not in ['ESC-50-master', 'checkpoint_results', 'results', 'mashups']])\n",
    "\n",
    "    for g in genres:\n",
    "        genre_path = os.path.join(search_path, g)\n",
    "        stems = []\n",
    "        for root, _, files in os.walk(genre_path):\n",
    "            for file in files:\n",
    "                if file.lower().endswith('.wav'):\n",
    "                    stems.append(os.path.join(root, file))\n",
    "        if stems: genre_data[g] = stems\n",
    "    return genre_data\n",
    "\n",
    "class CleanMusicDataset(torch.utils.data.Dataset):\n",
    "    def __init__(self, file_list, labels, extractor):\n",
    "        self.file_list = file_list\n",
    "        self.labels = labels\n",
    "        self.extractor = extractor\n",
    "\n",
    "    def __len__(self): return len(self.file_list)\n",
    "\n",
    "    def __getitem__(self, idx):\n",
    "        path = self.file_list[idx]\n",
    "        label = self.labels[idx]\n",
    "        \n",
    "        waveform, sr = torchaudio.load(path)\n",
    "        if sr != 16000:\n",
    "            resampler = torchaudio.transforms.Resample(sr, 16000)\n",
    "            waveform = resampler(waveform)\n",
    "        if waveform.shape[0] > 1: waveform = waveform.mean(dim=0, keepdim=True)\n",
    "        \n",
    "        # Take a 30s slice (or pad if shorter)\n",
    "        audio = waveform.squeeze(0).numpy()\n",
    "        target_samples = 16000 * TARGET_DURATION\n",
    "        if len(audio) > target_samples: audio = audio[:target_samples]\n",
    "        else: audio = np.pad(audio, (0, target_samples - len(audio)))\n",
    "        \n",
    "        inputs = self.extractor(\n",
    "            audio, \n",
    "            sampling_rate=16000, \n",
    "            return_tensors=\"pt\", \n",
    "            padding=\"max_length\", \n",
    "            max_length=16000 * TARGET_DURATION, \n",
    "            truncation=True\n",
    "        )\n",
    "        return {\"input_values\": inputs.input_values.squeeze(0), \"labels\": torch.tensor(label)}\n",
    "\n",
    "def compute_metrics(eval_pred):\n",
    "    logits, labels = eval_pred\n",
    "    predictions = np.argmax(logits, axis=-1)\n",
    "    return {\"accuracy\": accuracy_score(labels, predictions), \"f1\": f1_score(labels, predictions, average=\"macro\")}\n",
    "\n",
    "def main():\n",
