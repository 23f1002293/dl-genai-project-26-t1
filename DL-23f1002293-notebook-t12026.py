    "    \n",
    "    id2label = model.config.id2label\n",
    "    with torch.no_grad():\n",
    "        for _, row in tqdm(test_df.iterrows(), total=len(test_df)):\n",
    "            try:\n",
    "                path = os.path.join(DATA_DIR, row['filename'])\n",
    "                waveform, sr = torchaudio.load(path)\n",
    "                if sr != 16000:\n",
    "                    resampler = torchaudio.transforms.Resample(sr, 16000)\n",
    "                    waveform = resampler(waveform)\n",
    "                audio = waveform.mean(dim=0).numpy()\n",
    "                \n",
    "                target_samples = 16000 * TARGET_DURATION\n",
    "                inputs = extractor(\n",
    "                    audio[:target_samples], \n",
    "                    sampling_rate=16000, \n",
    "                    return_tensors=\"pt\",\n",
    "                    padding=\"max_length\",\n",
    "                    max_length=16000 * TARGET_DURATION,\n",
    "                    truncation=True\n",
    "                ).to(device)\n",
    "                \n",
    "                logits = model(inputs.input_values).logits\n",
    "                pred = torch.argmax(logits).item()\n",
    "                genre = id2label[pred]\n",
    "            except Exception as e:\n",
    "                genre = genres[0] # Fallback\n",
    "                \n",
    "            results.append({\"id\": row['id'], \"genre\": genre})\n",
    "            \n",
    "    pd.DataFrame(results).sort_values(by='id').to_csv(SUBMISSION_PATH, index=False)\n",
    "    print(f\"Submission saved to {SUBMISSION_PATH}\")\n",
