    "def preload_audio(file_list, target_sr, max_duration=10):\n",
    "    cache = {}\n",
    "    target_samples = int(target_sr * max_duration)\n",
    "    print(f\"Preloading {len(file_list)} files...\")\n",
    "    \n",
    "    for path in tqdm(file_list):\n",
    "        try:\n",
    "            try:\n",
    "                waveform, sr = torchaudio.load(path)\n",
    "            except:\n",
    "                import librosa\n",
    "                audio_np, sr = librosa.load(path, sr=None, mono=True)\n",
    "                waveform = torch.from_numpy(audio_np).unsqueeze(0)\n",
    "\n",
    "            if sr != target_sr:\n",
    "                resampler = torchaudio.transforms.Resample(sr, target_sr)\n",
    "                waveform = resampler(waveform)\n",
    "            if waveform.shape[0] > 1:\n",
    "                waveform = waveform.mean(dim=0, keepdim=True)\n",
    "            \n",
    "            audio = waveform.squeeze(0).numpy()\n",
    "            \n",
    "            if len(audio) > target_samples:\n",
    "                audio = audio[:target_samples]\n",
    "            elif len(audio) < target_samples:\n",
    "                audio = np.pad(audio, (0, target_samples - len(audio)))\n",
    "            \n",
    "            cache[path] = audio.astype(np.float16)\n",
    "        except Exception as e:\n",
    "            continue\n",
    "    return cache"
