    "class HubertClassifier(nn.Module):\n",
    "    def __init__(self, num_classes=10):\n",
    "        super().__init__()\n",
    "        self.hubert = HubertModel.from_pretrained(CFG.MODEL_NAME)\n",
    "        \n",
    "\n",
    "        self.gru = nn.GRU(\n",
    "            input_size=768,\n",
    "            hidden_size=384,\n",
    "            num_layers=2,\n",
    "            batch_first=True,\n",
    "            dropout=0.3,\n",
    "            bidirectional=True\n",
    "        )\n",
    "\n",
    "        self.attn = nn.Sequential(\n",
    "            nn.Linear(768, 128),\n",
    "            nn.Tanh(),\n",
    "            nn.Linear(128, 1)\n",
    "        )\n",
    "\n",
    "        self.classifier = nn.Sequential(\n",
    "            nn.Linear(768, 256),\n",
    "            nn.BatchNorm1d(256),\n",
    "            nn.Dropout(0.4),\n",
    "            nn.ReLU(),\n",
    "            nn.Linear(256, num_classes)\n",
    "        )\n",
    "\n",
    "    def forward(self, x):\n",
    "        h = self.hubert(x).last_hidden_state\n",
    "        h, _ = self.gru(h)\n",
    "        \n",
    "        attn_weights = torch.softmax(self.attn(h), dim=1)\n",
    "        pooled = torch.sum(h * attn_weights, dim=1)\n",
    "        \n",
    "        return self.classifier(pooled)"
