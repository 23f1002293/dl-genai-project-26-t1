    "def train_ast(config):\n",
    "    mapping_path = os.path.join(config['precompute_dir'], 'mapping.csv')\n",
    "    df = pd.read_csv(mapping_path)\n",
    "    genres = sorted(df['genre'].unique())\n",
    "    n_classes = len(genres)\n",
    "    skf = StratifiedKFold(n_splits=config['folds'], shuffle=True, random_state=config['seed'])\n",
    "    \n",
    "    fe = AutoFeatureExtractor.from_pretrained(MODEL_NAME)\n",
    "    \n",
    "    for fold, (tr_idx, val_idx) in enumerate(skf.split(df, df['genre'])):\n",
    "        print(f\"\\nFold {fold}\")\n",
    "        train_ds = Subset(ASTDataset(mapping_path, fe), tr_idx)\n",
    "        val_ds = Subset(ASTDataset(mapping_path, fe), val_idx)\n",
    "        \n",
    "        train_loader = DataLoader(train_ds, batch_size=config['batch_size'], shuffle=True, num_workers=config['num_workers'])\n",
    "        val_loader = DataLoader(val_ds, batch_size=config['batch_size'], shuffle=False, num_workers=config['num_workers'])\n",
    "        \n",
    "        model = ASTForAudioClassification.from_pretrained(\n",
    "            MODEL_NAME, num_labels=n_classes, ignore_mismatched_sizes=True\n",
    "        ).to(config['device'])\n",
    "        \n",
    "        optimizer = torch.optim.AdamW(model.parameters(), lr=config['lr'])\n",
    "        scaler = torch.amp.GradScaler(device=config['device'].type, enabled=config['amp'])\n",
    "        \n",
    "        best_f1 = 0.0\n",
    "        for epoch in range(config['epochs']):\n",
    "            model.train()\n",
    "            for batch in train_loader:\n",
    "                x, y = batch\n",
    "                x, y = x.to(config['device']), y.to(config['device'])\n",
    "                optimizer.zero_grad()\n",
    "                with torch.amp.autocast(device_type=config['device'].type, enabled=config['amp']):\n",
    "                    outputs = model(x, labels=y)\n",
    "                    loss = outputs.loss\n",
    "                scaler.scale(loss).backward()\n",
    "                scaler.step(optimizer)\n",
    "                scaler.update()\n",
    "            \n",
    "            model.eval()\n",
    "            preds, trues = [], []\n",
    "            with torch.no_grad():\n",
    "                for batch in val_loader:\n",
    "                    x, y = batch\n",
    "                    x = x.to(config['device'])\n",
    "                    logits = model(x).logits\n",
    "                    preds.extend(logits.argmax(-1).cpu().tolist())\n",
    "                    trues.extend(y.tolist())\n",
    "            \n",
    "            f1 = f1_score(trues, preds, average='macro')\n",
    "            print(f\"Epoch {epoch} F1: {f1:.4f}\")\n",
    "            if f1 > best_f1:\n",
    "                best_f1 = f1\n",
    "                model.save_pretrained(os.path.join(config['checkpoints_dir'], f\"fold{fold}\"))\n"
