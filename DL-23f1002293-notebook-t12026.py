    "class MashupSimulatorDataset(Dataset):\n",
    "    def __init__(self, stems_list, stems_data, genre_indices, noise_cache, num_variants=1):\n",
    "        self.stems_data = stems_data \n",
    "        self.genre_indices = genre_indices \n",
    "        self.noise_cache = noise_cache\n",
    "        self.num_variants = num_variants\n",
    "        \n",
    "        self.samples_map = []\n",
    "        for g_idx, indices in self.genre_indices.items():\n",
    "            label_tensor = torch.tensor(g_idx, dtype=torch.long)\n",
    "            for idx in indices:\n",
    "                for _ in range(num_variants):\n",
    "                    self.samples_map.append((idx, label_tensor))\n",
    "        \n",
    "        random.shuffle(self.samples_map)\n",
    "\n",
    "    def __len__(self):\n",
    "        return len(self.samples_map)\n",
    "\n",
    "    def __getitem__(self, idx):\n",
    "        stem_idx, label_tensor = self.samples_map[idx]\n",
    "        \n",
    "        w_base = random.uniform(0.7, 1.0)\n",
    "        mixed = self.stems_data[stem_idx] * w_base\n",
    "        \n",
    "        pool = self.genre_indices[label_tensor.item()]\n",
    "        num_distractors = random.randint(1, 3) \n",
    "        if len(pool) > 1:\n",
    "            other_indices = random.sample(pool, min(num_distractors, len(pool)))\n",
    "            for o_idx in other_indices:\n",
    "                if o_idx != stem_idx:\n",
    "                    # Randomly weight the overlapping tracks\n",
    "                    w_other = random.uniform(0.3, 0.6)\n",
    "                    mixed = mixed + (self.stems_data[o_idx] * w_other)\n",
    "        \n",
    "        if self.noise_cache:\n",
    "            noise = self.noise_cache[random.randint(0, len(self.noise_cache)-1)]\n",
    "            w_noise = random.uniform(0.05, 0.2)\n",
    "            mixed = mixed + (noise * w_noise)\n",
    "\n",
    "        if random.random() < 0.6:\n",
