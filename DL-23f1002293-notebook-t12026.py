    "def run_submission_inference(model, feature_extractor, id2label, genres, device):\n",
    "    test_csv = os.path.join(DATA_DIR, \"test.csv\")\n",
    "    if not os.path.exists(test_csv):\n",
    "        print(\"Test CSV not found. Skipping inference.\")\n",
    "        return\n",
    "\n",
    "    test_df = pd.read_csv(test_csv)\n",
    "    if not {'id', 'filename'}.issubset(test_df.columns):\n",
    "        print(\"Missing required columns in test.csv\")\n",
    "        return\n",
    "\n",
    "    results = []\n",
    "    model.eval()\n",
    "\n",
    "    with torch.no_grad():\n",
    "        for _, row in tqdm(test_df.iterrows(), total=len(test_df), desc=\"Inference\"):\n",
    "            audio_path = os.path.join(DATA_DIR, row['filename'])\n",
    "            try:\n",
    "                if not os.path.exists(audio_path):\n",
    "                    raise FileNotFoundError(audio_path)\n",
