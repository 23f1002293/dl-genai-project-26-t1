    "                waveform = waveform.mean(dim=0, keepdim=True)\n",
    "            \n",
    "            audio = waveform.squeeze(0).numpy()\n",
    "            if len(audio) > target_samples: audio = audio[:target_samples]\n",
    "            elif len(audio) < target_samples: audio = np.pad(audio, (0, target_samples - len(audio)))\n",
