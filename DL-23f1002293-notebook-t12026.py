    "def preload_audio(file_list, target_sr, max_duration=10):\n",
    "    cache = {}\n",
    "    target_samples = int(target_sr * max_duration)\n",
    "    for path in tqdm(file_list):\n",
    "        try:\n",
    "            try:\n",
    "                waveform, sr = torchaudio.load(path)\n",
    "            except:\n",
    "                import librosa\n",
    "                audio_np, sr = librosa.load(path, sr=None, mono=True)\n",
    "                waveform = torch.from_numpy(audio_np).unsqueeze(0)\n",
    "\n",
    "            if sr != target_sr:\n",
    "                resampler = torchaudio.transforms.Resample(sr, target_sr)\n",
    "                waveform = resampler(waveform)\n",
