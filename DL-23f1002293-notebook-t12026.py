    "def load_audio(path, sr=SAMPLING_RATE, duration=DURATION):\n",
    "    if os.path.isdir(path):\n",
    "        wavs = []\n",
    "        for p in glob.glob(os.path.join(path, '*.wav')):\n",
    "            y, orig_sr = sf.read(p)\n",
    "            if y.ndim > 1: y = np.mean(y, axis=1)\n",
    "            if orig_sr != sr: y = librosa.resample(y.astype(float), orig_sr=orig_sr, target_sr=sr)\n",
    "            wavs.append(y.astype(np.float32))\n",
    "        if not wavs: return np.zeros(int(sr * duration), dtype=np.float32)\n",
    "        mix = np.zeros(max(len(x) for x in wavs), dtype=np.float32)\n",
    "        for x in wavs: mix[:len(x)] += x\n",
    "        maxv = np.max(np.abs(mix))\n",
    "        if maxv > 0: mix /= maxv\n",
    "    else:\n",
    "        mix, orig_sr = sf.read(path)\n",
    "        if mix.ndim > 1: mix = np.mean(mix, axis=1)\n",
    "        if orig_sr != sr: mix = librosa.resample(mix.astype(float), orig_sr=orig_sr, target_sr=sr)\n",
    "    \n",
    "    target_len = int(duration * sr)\n",
    "    if len(mix) < target_len:\n",
    "        mix = np.pad(mix, (0, target_len - len(mix)))\n",
    "    else:\n",
    "        mix = mix[:target_len]\n",
    "    return mix.astype(np.float32)\n",
    "\n",
    "def build_noise_bank(noise_root):\n",
    "    noise_files = []\n",
    "    meta = os.path.join(noise_root, 'meta', 'esc50.csv')\n",
    "    audio_folder = os.path.join(noise_root, 'audio')\n",
    "    if os.path.exists(meta) and os.path.isdir(audio_folder):\n",
    "        df = pd.read_csv(meta)\n",
    "        for _, row in df.iterrows():\n",
    "            fpath = os.path.join(audio_folder, row['filename'])\n",
    "            if os.path.exists(fpath):\n",
    "                noise_files.append(fpath)\n",
    "    return noise_files\n",
    "\n",
    "def add_noise(y, noise_bank, sr=SAMPLING_RATE):\n",
    "    if not noise_bank: return y\n",
    "    noise_path = random.choice(noise_bank)\n",
    "    noise_y, nsr = sf.read(noise_path)\n",
    "    if noise_y.ndim > 1: noise_y = np.mean(noise_y, axis=1)\n",
    "    if nsr != sr: noise_y = librosa.resample(noise_y.astype(float), orig_sr=nsr, target_sr=sr)\n",
    "    \n",
    "    if len(noise_y) < len(y):\n",
    "        noise_y = np.pad(noise_y, (0, len(y) - len(noise_y)))\n",
    "    else:\n",
    "        start = random.randint(0, len(noise_y) - len(y))\n",
    "        noise_y = noise_y[start:start+len(y)]\n",
    "    \n",
    "    snr = random.uniform(CONFIG['noise_snr_min'], CONFIG['noise_snr_max'])\n",
    "    rms_y = np.sqrt(np.mean(y**2)) + 1e-9\n",
    "    rms_n = np.sqrt(np.mean(noise_y**2)) + 1e-9\n",
    "    alpha = rms_y / (rms_n * (10**(snr/20)))\n",
    "    return (y + alpha * noise_y).astype(np.float32)"
