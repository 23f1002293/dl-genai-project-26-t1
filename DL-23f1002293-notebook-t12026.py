    "            if self.noise_files:\n",
    "                try:\n",
    "                    noise = self.load_audio(random.choice(self.noise_files))\n",
    "                    mix += noise * random.uniform(0.01, 0.06)\n",
    "                except:\n",
    "                    pass\n",
    "\n",
    "        return mix, label"
