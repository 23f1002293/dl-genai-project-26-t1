    "\n",
    "    feature_extractor = AutoFeatureExtractor.from_pretrained(MODEL_ID)\n",
    "    model = AutoModelForAudioClassification.from_pretrained(MODEL_ID, num_labels=10, ignore_mismatched_sizes=True).to(device)\n",
    "\n",
    "    if not SKIP_TRAINING:\n",
    "        all_genre_files = [p for paths in genre_data.values() for p in paths]\n",
    "        train_files, val_files, train_labels, val_labels = train_test_split(all_files, all_labels, test_size=0.1, random_state=42)\n",
    "        train_ds = CleanMusicDataset(train_files, train_labels, feature_extractor)\n",
    "        val_ds = CleanMusicDataset(val_files, val_labels, feature_extractor)\n",
    "        \n",
    "        args = TrainingArguments(\n",
    "            output_dir=\"./results\",\n",
    "            num_train_epochs=EPOCHS,\n",
    "            per_device_train_batch_size=BATCH_SIZE,\n",
    "            learning_rate=LEARNING_RATE,\n",
    "            eval_strategy=\"epoch\",\n",
    "            save_strategy=\"epoch\",\n",
    "            load_best_model_at_end=True,\n",
    "            fp16=torch.cuda.is_available(),\n",
    "            logging_steps=10\n",
    "        )\n",
    "        trainer = Trainer(model=model, args=args, train_dataset=train_ds, eval_dataset=val_ds, compute_metrics=compute_metrics)\n",
    "        trainer.train()\n",
    "\n",
    "    test_csv = os.path.join(DATA_DIR, \"test.csv\")\n",
    "    test_df = pd.read_csv(test_csv)\n",
    "    results = []\n",
